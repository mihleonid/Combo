<h2>Основные понятия и теоремы комбинаторики</h2>
<ul>
<li>Основные правила комбинаторики.</li>
<li>Размещения с повторениями.</li>
<li>Размещения без повторений.
<p>Имеется n различных предметов. Сколько из них
можно составить k-расстановок? При этом две расстановки считаются различными, если они либо отличаются
друг от друга хотя бы одним элементом, либо состоят
из одних и тех же элементов, но расположенных в разном порядке.
</p><p>
Такие расстановки называют размещениями без
повторений, а их число обозначают А<sup>k</sup><sub>n</sub>. При составлении k-размещений без повторений из n предметов нам
надо сделать k выборов. На первом шагу можно выбрать любой из имеющихся n предметов. Если этот
выбор уже сделан, то на втором шагу приходится выбирать из оставшихся n-1 предметов - ведь повторять сделанный выбор нельзя (напомним, что в отличие от случая размещений с повторениями y нас теперь есть лишь один элемент каждого вида). Точно так же на
третьем шагу для выбора остается лишь n-2 свободных предметов, на четвертом n-3 предметов... на
k-м шагу n-k+1 предметов. Поэтому по правилу произведения получаем, что число k-размещений без пoвторения из n предметов выражается следующим образом:
</p><p>
А<sup>k</sup><sub>n</sub> = n(n-1)...(n-k+1).</p>
</li>
<li>Перестановки без повторений.
<p>Формулировка у термина <q>перестановки без повторений</q> схоже с формулировкой термина <q>Размещения без повторений</q>. Поэтому она здесь не приводится.</p>
<p>Количество перестановок без повторений n элементов - это А<sup>n</sup><sub>n</sub></p>
<p>
P<sub>n</sub>=А<sup>n</sup><sub>n</sub> = n(n-1)...(n-n+1) = n*(n-1) *...* 2 * 1 = n!
</p>
</li>
