<p><big>Главная теорема комбинаторики</big> – это теорема о <b>включениях и исключениях</b>.</p>
<p>Попробуем пояснить на примере решения задачи про наш класс. У нас в классе учатся 26 человек. Из них 10 более интересуются математикой, 17 - литературой и 5 – тем и другим предметом. Сколько человек не интересуются ни математикой, ни литературой?</p>
<p>Результат можно получить следующим образом. Построим диаграмму, на которой изобразим овал, соответствующий общему числу учеников (26) и две пересекающиеся области А и В по 10 и 17 человек (более интересующихся математикой и литературой). На диаграмме общая часть этих двух областей соответствует 5 - количеству учеников, интересуются обоими предметами. Требуется найти количество учеников, которых не интересует ни математика, ни литература, то есть определить область, не входящую ни в область А, ни в область В.</p>
